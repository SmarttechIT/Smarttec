package encapsulation;

public class Encapsul {
	
	private int a;
	private int b;
	private int c;
	// after this line right click and go to Source then select Generate getter and setter Methods
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	public int getC() {
		return c;
	}
	public void setC(int c) {
		this.c = c;
	}
	
	
	
}
