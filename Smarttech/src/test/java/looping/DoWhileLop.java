package looping;



//While(Boolean Expression) if the condition is true then it will go inside the loop. If the condition is false it will terminate from the loop (outside the loop)

public class DoWhileLop {
	
	
	public static void main(String[] args) {
		
		int a=20;
		
		do {
			System.out.println(a);
			a++;
		
		}while (a<=30);
		
	}
	

}
